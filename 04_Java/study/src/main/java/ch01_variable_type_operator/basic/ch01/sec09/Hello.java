// alt + shift + 방향키(위/아래) : 라인 이동
// alt + 1 : 패키지 구조 이동
// ctrl + / : 한 줄 주석
/* ctrl + shift + / :
* 범위
* 주석 */

// class명은 대문자로 시작해야 함
public class Hello {
    // psvm 후 Enter : main 메소드 단축키
    // main 메소드 : 자바를 실행시키기 위해 필요한 메소드 (없으면 실행 안 됨, main메소드부터 순차적 실행)
    // 실행하기 : shift + F10
    public static void main(String[] args) {
        // 출력문
        // System.out.print()   : 단순 출력 (출력 후 줄바꿈 X)
        // System.out.println() : 한 줄 출력 (출력 후 줄바꿈 O)

        // Hello Java 출력하기
        // sout : 프린트문 단축키
        // ctrl + d : 한 줄 복사
        System.out.println("Hello Java");
        System.out.println("Hello Java");
        System.out.print("Hello Java");
        System.out.print("Hello Java");
    }
}
